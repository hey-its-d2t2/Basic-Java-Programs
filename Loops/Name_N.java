/** A java program to print your name  upto N times*/
import java.util.Scanner;
class Name_N
{
	public static void main(String[] args)
	{
		Scanner sc = new Scanner(System.in);
		System.out.print("N = ");
		int n = sc.nextInt();
		for(int i=1;i<=n;i++)
			System.out.println("DEEPAK SINGH");
	}
}